class Movie:
    def __init__(self, title,
                 url,
                 productionhouses,
                 rating,
                 no_ratings,
                 reviews,
                 cast,
                 characters,
                 genre):
        self.title = title
        self.url = url
        self.productionhouses = productionhouses
        self.rating = rating
        self.no_ratings = no_ratings
        self.reviews = reviews
        self.cast = cast
        self.characters = characters
        self.genre = genre


